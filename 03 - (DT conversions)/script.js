//String conversion.
let score = "34a";
let number = Number(score);
console.log(number);

//NULL conversion.
let score1 = null;
let numbe1 = Number(score1);
console.log(numbe1);

//TO BOOLEAN conversion.
let booleanVal = true;
let boolNumber = Number(booleanVal);
console.log(boolNumber);

//FROM BOOLEAN CONVERSION (0 will be converted to false and vice versa.)
let boolVal1 = 1;
let boolNumber1 = Number(boolVal1);
console.log(boolNumber1);
